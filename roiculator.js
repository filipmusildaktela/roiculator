async function submitLeadModal() {
    const firstName = document.getElementById('leadFirstName').value.trim();
    const lastName = document.getElementById('leadLastName').value.trim();
    const company = document.getElementById('leadCompany').value.trim();
    const email = document.getElementById('leadEmail').value.trim();
    const errorEl = document.getElementById('leadModalError');
    errorEl.style.display = 'none';

    if (!firstName || !lastName || !company || !email) {
        errorEl.textContent = translator.translate('leadModal.validation.required');
        errorEl.style.display = 'block';
        return;
    }
    if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) {
        errorEl.textContent = translator.translate('leadModal.validation.email');
        errorEl.style.display = 'block';
        return;
    }

    const submitBtn = document.getElementById('leadModalSubmit');
    const originalText = submitBtn.textContent;
    submitBtn.disabled = true;
    submitBtn.textContent = translator.translate('leadModal.sending');

    const payload = {
        firstName, lastName, company, email,
        calculator: _pendingCalcFormData,
        language: translator.getCurrentLanguage(),
        timestamp: new Date().toISOString(),
        pageUrl: window.location.href
    };

    try {
        await fetch(MAKE_WEBHOOK_URL, {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify(payload)
        });
    } catch (err) {
        console.warn('Lead webhook failed:', err);
    }

    submitBtn.disabled = false;
    submitBtn.textContent = originalText;

    const dataToUse = _pendingCalcFormData;
    _pendingCalcFormData = null;
    closeLeadModal();
    runCalculationAndShow(dataToUse);
}

function getFormData() {
    const channels = {};
    document.querySelectorAll('input[name="channels"]:checked').forEach(cb => {
        channels[cb.value] = parseInt(document.getElementById(cb.value + '-count').value) || 0;
    });
    return {
        operators: parseInt(document.getElementById('operators').value),
        channels: channels,
        avgTime: parseInt(document.getElementById('avgTime').value),
        hourlyWage: parseInt(document.getElementById('hourlyWage').value),
        currency: document.getElementById('currency').value,
        repeatQueries: parseInt(document.getElementById('repeatQueries').value),
        industry: document.getElementById('industry').value
    };
}

function validateForm(data) {
    if (Object.keys(data.channels).length === 0) {
        alert(translator.translate('validation.channelsRequired'));
        return false;
    }
    return true;
}

function performCalculations(data) {
    const industry = industryData[data.industry];
    let totalQueries = 0;
    Object.keys(data.channels).forEach(k => totalQueries += data.channels[k]);

    const automationRate = industry.aiAutomationSavings;
    const repeatingQueriesRate = data.repeatQueries / 100;
    const automateableQueries = totalQueries * repeatingQueriesRate;
    const automatedQueries = automateableQueries * automationRate;
    const timeSavedMinutes = automatedQueries * data.avgTime;
    const timeSavedHours = timeSavedMinutes / 60;
    const totalSavings = timeSavedHours * data.hourlyWage * 12;

    return { totalSavings, totalTimeSaved: timeSavedHours * 12, industry, totalQueries };
}

function generateDynamicInsight(template, i, a, c) {
    return template.replace(/\{integration\}/g, i + '%').replace(/\{ai\}/g, a + '%').replace(/\{combined\}/g, c + '%');
}

function formatCurrency(amount, currency) {
    const config = currencyConfig[currency];
    if (currency === 'HUF' || currency === 'RSD') {
        return new Intl.NumberFormat(config.locale, { minimumFractionDigits: 0, maximumFractionDigits: 0 }).format(amount) + ' ' + config.symbol;
    }
    return new Intl.NumberFormat(config.locale, { style: 'currency', currency: currency, minimumFractionDigits: 0, maximumFractionDigits: 0 }).format(amount);
}

function displayResults(results, formData) {
    document.getElementById('totalSavings').textContent = formatCurrency(results.totalSavings, formData.currency);

    const industryTranslated = translator.getIndustryData(formData.industry);
    const selInd = industryData[formData.industry];
    const intPct = Math.round(selInd.integrationSavings * 100);
    const aiPct = Math.round(selInd.aiAutomationSavings * 100);
    const comPct = Math.round((selInd.integrationSavings + selInd.aiAutomationSavings) * 100);

    let insight = industryTranslated?.insight || 'No insight';
    insight = generateDynamicInsight(insight, intPct, aiPct, comPct);
    insight = insight.replace(/(\d+%)/g, '<strong style="color: var(--dkt-red-orange);">$1</strong>');

    document.getElementById('industryInsight').innerHTML = `<h4>${translator.translate('industries.' + formData.industry + '.name')}</h4><p>${insight}</p>`;
    document.getElementById('channelBreakdown').style.display = 'none';
    displayCaseStudyBanner(formData.industry);
    createSavingsProgressChart(results, formData);
}

