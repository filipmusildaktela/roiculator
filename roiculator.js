// 2. LOGIC CLASSES
class Translator {
    constructor() { this.currentLanguage = 'en'; }
    setLanguage(language) {
        if (window.translations && window.translations[language]) {
            this.currentLanguage = language;
            localStorage.setItem('preferred-language', language);
            this.translatePage();
            this.updateDynamicContent();
        }
    }
    translate(key, fallback = null) {
        const keys = key.split('.');
        let value = window.translations && window.translations[this.currentLanguage];
        for (const k of keys) {
            if (value && typeof value === 'object' && k in value) value = value[k];
            else {
                value = window.translations && window.translations['en'];
                for (const fallbackKey of keys) { if (value && typeof value === 'object') value = value[fallbackKey]; else { value = undefined; break; } }
                break;
            }
        }
        return typeof value === 'string' ? value : (fallback || key);
    }
    translatePage() {
        document.querySelectorAll('[data-translate]').forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = this.translate(key);
            if (element.tagName === 'INPUT' && (element.type === 'text' || element.type === 'email' || element.type === 'number')) {
                if (key.includes('Placeholder')) element.placeholder = translation;
            } else if (element.tagName === 'OPTION') element.textContent = translation;
            else element.textContent = translation;
        });
        document.querySelectorAll('[data-translate-title]').forEach(element => {
            const key = element.getAttribute('data-translate-title');
            element.title = this.translate(key);
        });
    }
    updateDynamicContent() { if (typeof updateSliderLabel === 'function') updateSliderLabel(); }
    getIndustryData(industryKey) {
        let lang = this.currentLanguage;
        if (window.translations[lang]?.industries?.[industryKey]) return window.translations[lang].industries[industryKey];
        return window.translations['en']?.industries?.[industryKey] || { name: industryKey, insight: 'No insight available' };
    }
    getCurrentLanguage() { return this.currentLanguage; }
    detectLanguageFromURL() {
        const supported = ['cs', 'sk', 'pl', 'sr', 'hu', 'ro'];
        const segments = window.location.pathname.split('/').filter(Boolean);
        if (segments.length === 0) return null;
        const first = segments[0].toLowerCase();
        return supported.includes(first) ? first : null;
    }
    initialize() {
        const lang = this.detectLanguageFromURL() || 'en';
        this.setLanguage(lang);
        document.getElementById('languageSelector').value = lang;
    }
}
